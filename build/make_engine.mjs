/* Generate app/engine.js from the VALIDATED numerics in build/template.html so
   the Expo app and the HTML artifact share byte-identical math (no drift).
   The extracted functions are cross-checked against the NumPy reference by
   build/validate_js.mjs. */
import fs from 'node:fs';
const tpl = fs.readFileSync('build/template.html', 'utf8');
function grab(name){
  const s = tpl.indexOf('function ' + name + '(');
  if (s < 0) throw new Error('missing ' + name);
  const o = tpl.indexOf('{', s);
  let d = 0, i = o;
  for (; i < tpl.length; i++){ const c = tpl[i]; if (c === '{') d++; else if (c === '}'){ d--; if (!d){ i++; break; } } }
  return tpl.slice(s, i);
}
const NAMES = ['simpleReturns','mean','sampleStd','sharpeMomentum','momentumDaily',
  'annualizeStats','olsFit','momentumScore','covMatrix','corrFromCov',
  'quasiDiagOrder','clusterVar','recursiveBisection','hrpWeights',
  'equalWeights','inverseVolWeights','matInverse','minVarWeights','applyCaps',
  'periodReturn','pct','signPct','fmtSharpe','fmtCap','clsOf'];
const header = `/* AUTO-GENERATED by build/make_engine.mjs from build/template.html — do not edit.
   Sharpe momentum + HRP + constraint numerics, cross-checked against the NumPy
   reference (build/reference_numerics.py) via build/validate_js.mjs. */
const TD = 252;
`;
const body = NAMES.map(grab).join('\n\n');
const footer = `\n\nexport { ${NAMES.join(', ')}, TD };\n`;
fs.mkdirSync('app', { recursive: true });
fs.writeFileSync('app/engine.js', header + body + footer);
console.log('wrote app/engine.js with', NAMES.length, 'functions');
